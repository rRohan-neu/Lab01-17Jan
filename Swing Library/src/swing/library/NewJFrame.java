    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:

        
       try {
           
        firstName = this.firstNameTextField.getText();
        lastName = this.lastNameTextField.getText();
        email =  this.emailTextField.getText();
        gender = this.genderbuttonGroup.getSelection().getActionCommand();
        number = this.phoneNumberTextField.getText();
        age = this.ageTextField.getText();
        
        message = "User Profile:\n" + "Name: " + firstName + " " + lastName + "\n" +
                         "Gender: " + gender + "\n" +
                         "Age: " + age + "\n" +
                         "Phone: " + number + "\n" +
                         "Email: " + email;
             
       
        
          if(firstName.isEmpty() || lastName.isEmpty() || gender.isEmpty() || age.isEmpty() || number.isEmpty() || email.isEmpty()){
                                   JOptionPane.showMessageDialog(rootPane, "All fields must be filled, try again.", "Failed!", HEIGHT);

        } 
          
          if (!age.matches("\\d+") || Integer.parseInt(age) < 0) {
    JOptionPane.showMessageDialog(rootPane, "Enter valid Age ", "Validation Error", JOptionPane.ERROR_MESSAGE);
    return;
}
          if (!number.matches("\\d{10}")) {
    JOptionPane.showMessageDialog(rootPane, "Phone number must be 10 digits!", "Validation Error", JOptionPane.ERROR_MESSAGE);
    return;
   
}
           if (!email.matches("^[A-Za-z0-9+_.-]+@(.+)$")) {
            JOptionPane.showMessageDialog(this, "Enter a valid email address!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
           
          else {
       
       JOptionPane.showMessageDialog(rootPane, "Form has been submitted for " +message, "Success!!", HEIGHT, this.pictureButton.getIcon());
          
          }
          
          
      
           
       } 
       
       catch (NullPointerException ne){
           
                JOptionPane.showMessageDialog(rootPane, "Enter all the fields and try again!", "Failed!", HEIGHT);
  
           
       } 
       
       
    }//GEN-LAST:event_submitButtonActionPerformed

